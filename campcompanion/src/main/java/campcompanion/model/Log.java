package campcompanion.model;

public class Log {

	// ATTRIBUTES
	private String comment;
	private int note;

	// GETTERS AND SETTERS
	public String getComment() {
		return comment;
	}

	public void setComment(String comment) {
		this.comment = comment;
	}

	public int getNote() {
		return note;
	}

	public void setNote(int note) {
		this.note = note;
	}

	// METHODS

}
